
"""Listas en  Python"""

"""Listas remove(): Elimina un elemento de las lista indicando el valor"""

var_1 = ["Python", "Java", "PHP", "Ruby", "Javascript", "Typescript"]

print("Mi lista var_1 es: {}".format(var_1))

var_1.remove("PHP")
var_1.remove("Ruby")

print("Mi lista actualizada es: {}".format(var_1))
