
"""Tipos de datos trascendentales"""
"""Tipos de datos: Lista []"""

var1 = [20, "Luis", "Calle Las Almendras", "1458"]
print("El valor de mi varaible var1 es: {}".format(var1))

"""Tipos de datos: Tuplas ()"""

var2 = (40, 80.6, 170, 2024, "Hola Pythonista!!!")
print("El valor de mi variable var2 es: {}".format(var2))

"""Tipos de datos: Set ()"""
var3 = set(["Bienvenidos", 50, 150, 40.79, False, 50, 50, 150])
print("El valor de mi variable var3 es: {}".format(var3))


"""Tipos de datos: Diccionarios {}"""
var4 = {"nombre": "Carlos", "edad": 28, "dni": 45217896}
print("El valor de mi variable var4 es: {}".format(var4))
